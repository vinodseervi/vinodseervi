// Service Worker for Vinod Seervi's Portfolio
const CACHE_NAME = 'vinod-seervi-portfolio-v1';
const urlsToCache = [
    '/',
    '/index.html',
    '/css/modern-styles.css',
    '/js/modern-script.js',
    '/About Me/pic3.jpg',
    '/img/bc1.jpg',
    '/img/bc img2.jpg',
    '/box/jaipur.jpg',
    '/box/jodhpur.jpg',
    '/box/udiapur.jpg',
    '/box/jaisalmer.jpg',
    '/box/mount abu.jpg',
    '/box/pali.jpg',
    'https://fonts.googleapis.com/css2?family=Inter:wght@300;400;500;600;700;800&family=Space+Grotesk:wght@300;400;500;600;700&display=swap',
    'https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.4.0/css/all.min.css',
    'https://unpkg.com/aos@2.3.1/dist/aos.css',
    'https://unpkg.com/aos@2.3.1/dist/aos.js'
];

// Install event
self.addEventListener('install', function(event) {
    event.waitUntil(
        caches.open(CACHE_NAME)
            .then(function(cache) {
                console.log('Opened cache');
                return cache.addAll(urlsToCache);
            })
    );
});

// Fetch event
self.addEventListener('fetch', function(event) {
    event.respondWith(
        caches.match(event.request)
            .then(function(response) {
                // Return cached version or fetch from network
                return response || fetch(event.request);
            }
        )
    );
});

// Activate event
self.addEventListener('activate', function(event) {
    event.waitUntil(
        caches.keys().then(function(cacheNames) {
            return Promise.all(
                cacheNames.map(function(cacheName) {
                    if (cacheName !== CACHE_NAME) {
                        console.log('Deleting old cache:', cacheName);
                        return caches.delete(cacheName);
                    }
                })
            );
        })
    );
});
